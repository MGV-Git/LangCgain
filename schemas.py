"""Схема финального ответа агента.

Отдельный файл, чтобы класс импортировался как `schemas.ResponseFormat`: LangGraph
пишет тип в чекпоинт; для `__main__.ResponseFormat` путь нестабилен при разных способах запуска.
"""

from pydantic import BaseModel, Field


class ResponseFormat(BaseModel):
    """Один абзац ответа пользователю — без разбиения на поля в интерфейсе."""

    text: str = Field(
        ...,
        description=(
            "Единый связный ответ по-русски: погода, место, при необходимости лёгкий каламбур "
            "в том же тексте. Только русский язык, без английских предложений."
        ),
    )
